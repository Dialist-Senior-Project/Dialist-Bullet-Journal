package com.example.dialist;

public class DBColor {
    public int c1;
    public int c2;
    public int c3;
    public int c4;
    public int c5;
    public int c;

    public DBColor(){}

    public DBColor(int c1, int c2, int c3, int c4, int c5, int c){
        this.c1 = c1;
        this.c2 = c2;
        this.c3 = c3;
        this.c4 = c4;
        this.c5 = c5;
        this.c = c;
    }

    public int getc1(){ return c1; }

    public int setc1(int c1){ return c1; }

    public int getc2(){ return c2; }

    public int setc2(int c1){ return c2; }

    public int getc3(){ return c3; }

    public int setc3(int c3){ return c3; }

    public int getc4(){ return c4; }

    public int setc4(int c4){ return c4; }

    public int getc5(){ return c5; }

    public int setc5(int c5){ return c5; }

    public int getc(){ return c; }

    public int setc(int c){ return c; }
}
